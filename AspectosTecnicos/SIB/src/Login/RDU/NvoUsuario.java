    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        int conf = JOptionPane.showConfirmDialog(rootPane, "Sus respuestas deben ser exactas, cada letra, signo, número, espacio, minúscula o minúscula\n" + 
                "será verificada cuando se le soliciten al modificar o recuperar su contraseña",
                "Verifique que todos sus datos sean correctos",JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
        
        // El usuario da la confirmación de que sí desea guardar los datos como están
        if(conf == 0){
            nomUsuario = campoUsuario.getText();
            contra = campoContra.getText();
            pregunta1 = campoPregunta1.getText();
            pregunta2 = campoPregunta2.getText();
            pregunta3 = campoPregunta3.getText();
            pregunta4 = campoPregunta4.getText();
            respuesta1 = campoRespuesta1.getText();
            respuesta2 = campoRespuesta2.getText();
            respuesta3 = campoRespuesta3.getText();
            respuesta4 = campoRespuesta4.getText();
            
                Aconexion cn = new Aconexion();
                boolean verificadorR = cn.RegistrarUsuario(nomUsuario, contra, pregunta1,
                        pregunta2, pregunta3, pregunta4, respuesta1, respuesta2, respuesta3, respuesta4);
                
                    if(verificadorR != false){
                        Login l = new Login();
                        l.setVisible(true);
                        dispose();
                        JOptionPane.showMessageDialog(rootPane, "El usuario: '" + nomUsuario + "', se ha registrado exitosamente en el sistema\n" +
                                "¡Ahora puede iniciar sesión!",
                                "¡Éxito!", WIDTH);
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "El usuario no se ha podido registrar por causas desconocodas, si persiste el problema contacte al desarrolador de la aplicación",
                                "¡Éxito!", JOptionPane.ERROR_MESSAGE);
                    }
        }
        
    }//GEN-LAST:event_botonGuardarActionPerformed

