    private void botonVcontraseñasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVcontraseñasActionPerformed
                            contra = campoContra.getText();
                            contra2 = campoContra2.getText();
                                if(!contra2.equals(contra)){
                                    JOptionPane.showMessageDialog(rootPane, 
                                            "Las contraseñas no coinciden, verifiquelas y vuelva a intentar", "Mensaje", JOptionPane.ERROR_MESSAGE);
                                    campoContra.setText("");
                                    campoContra2.setText("");
                                } else{
                                    campoUsuario.setEnabled(false);
                                    campoContra.setEnabled(false);
                                    campoContra2.setEnabled(false);
                                            pregunta1 = campoPregunta1.getText();
                                            pregunta2 = campoPregunta2.getText();
                                            pregunta3 = campoPregunta3.getText();
                                            pregunta4 = campoPregunta4.getText();
                                            respuesta1 = campoRespuesta1.getText();
                                            respuesta2 = campoRespuesta2.getText();
                                            respuesta3 = campoRespuesta3.getText();
                                            respuesta4 = campoRespuesta4.getText();
                                            
                                            // Activación de campos de preguntas
                                            campoPregunta1.setEnabled(true);
                                        if(respuesta1.isEmpty()){
                                            if(pregunta2.isEmpty()){
                                                campoPregunta2.setEnabled(false);
                                            }else{
                                                campoPregunta2.setEnabled(true);
                                            }
                                        } else{
                                            campoPregunta2.setEnabled(true);
                                        }
                                        if(respuesta2.isEmpty()){
                                            if(pregunta3.isEmpty()){
                                                campoPregunta3.setEnabled(false);
                                            }else{
                                                campoPregunta3.setEnabled(true);
                                            }
                                        } else{
                                            campoPregunta3.setEnabled(true);
                                        }
                                        if(respuesta3.isEmpty()){
                                            if(pregunta4.isEmpty()){
                                                campoPregunta4.setEnabled(false);
                                            }else{
                                                campoPregunta4.setEnabled(true);
                                            }
                                        } else{
                                            campoPregunta4.setEnabled(true);
                                        }
                                            // Activación de campos de respuestas
                                        if(pregunta1.isEmpty()){
                                            campoRespuesta1.setEnabled(false);
                                        } else{
                                                    campoRespuesta1.setEnabled(true);
                                        }
                                        if(pregunta2.isEmpty()){
                                            campoRespuesta2.setEnabled(false);
                                        } else{
                                                    campoRespuesta2.setEnabled(true);
                                        }
                                        if(pregunta3.isEmpty()){
                                            campoRespuesta3.setEnabled(false);
                                        } else{
                                                    campoRespuesta3.setEnabled(true);
                                        }
                                        if(pregunta4.isEmpty()){
                                            campoRespuesta4.setEnabled(false);
                                        } else{
                                                    campoRespuesta4.setEnabled(true);
                                        }
                                        
                                            botonGuardar.setVisible(true);
                                            if(!pregunta1.isEmpty() & !pregunta2.isEmpty() & !pregunta3.isEmpty() & !pregunta4.isEmpty() & 
                                                    !respuesta1.isEmpty() & !respuesta2.isEmpty() & !respuesta3.isEmpty() & !respuesta4.isEmpty()){
                                                                    botonGuardar.setEnabled(true);    
                                            }else{
                                                botonGuardar.setEnabled(false);
                                            }
                                            botonModDU.setVisible(true);
                                           
                                            botonVcontraseñas.setEnabled(true);
                                            botonVcontraseñas.setVisible(false);
                                    JOptionPane.showMessageDialog(rootPane, 
                                            "¡Las contraseñas coinciden correctamente!", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                                }
    }//GEN-LAST:event_botonVcontraseñasActionPerformed

